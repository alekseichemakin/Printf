#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;
	char	c;

	i = 0;
	if (s != 0)
	{
		while (s[i])
		{
			c = s[i];
			write(fd, &c, 1);
			i++;
		}
		c = '\n';
		write(fd, &c, 1);
	}
}
