#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		n;
	char	*p;

	p = (char *)s;
	n = 0;
	while (s[n] != c)
	{
		if (s[n] == '\0')
			return (NULL);
		n++;
	}
	return (&p[n]);
}
